sprites.onOverlap(SpriteKind.Player, SpriteKind.boss, function (sprite, otherSprite) {
    if (Iframes == 0) {
        if (otherSprite.vx >= 0) {
            sprite.vx = -200
            sprite.vy = -200
        } else if (otherSprite.vx <= 0) {
            sprite.vx = 200
            sprite.vy = -200
        } else {
            sprite.vy = -200
        }
        info.changeLifeBy(-1)
        Iframes = 1
        timer.after(2000, function () {
            Iframes = 0
        })
    }
})
